const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [location, setLocation] = useLocation();
  
  // Get language from URL path
  const [language, setLanguageState] = useState<Language>(() => {
    if (typeof window !== 'undefined') {
      const urlLang = getLanguageFromPath(window.location.pathname);
      if (SUPPORTED_LANGUAGES.includes(urlLang)) {
        return urlLang;
      }
    }
    return 'en';
  });

  // Sync language with URL changes
  useEffect(() => {
    const urlLang = getLanguageFromPath(location);
    if (SUPPORTED_LANGUAGES.includes(urlLang) && urlLang !== language) {
      setLanguageState(urlLang);
      document.documentElement.lang = urlLang;
    }
  }, [location, language]);

  const setLanguage = useCallback((lang: Language) => {
    if (lang !== language) {
      setLanguageState(lang);
      if (typeof window !== 'undefined') {
        localStorage.setItem('language', lang);
        document.documentElement.lang = lang;
        // Navigate to new language URL, preserving subpath and hash
        const currentPath = window.location.pathname;
        const hash = window.location.hash;
        // Strip base path prefix if present (dynamic: '/cyber' in prod, '' in dev)
        const pathWithoutBase = BASE_PATH && currentPath.startsWith(BASE_PATH) ? currentPath.slice(BASE_PATH.length) : currentPath;
        // Extract subpath after /{lang}/ (e.g., "advisor", "socaas")
        const subpathMatch = pathWithoutBase.match(/^\/(de|en|fr)\/(.+)$/);
        const subpath = subpathMatch ? subpathMatch[2] : '';
        const newPath = subpath ? `/${lang}/${subpath}${hash}` : `/${lang}/assistance${hash}`;
        setLocation(newPath);
      }
    }
  }, [language, setLocation]);

  const t = useCallback((key: string): string => {
    return translations[language][key] || key;
  }, [language]);

  // Get asset URL with language and base path for proxy compatibility
  const getAssetUrl = useCallback((path: string): string => {
    const cleanPath = path.startsWith('/') ? path.slice(1) : path;
    // Static assets (images, audio, pdf) should use base path relative URL
    // This ensures they work in subpath deployments
    const viteBase = import.meta.env.BASE_URL || '/';
    if (cleanPath.startsWith('images/') || cleanPath.startsWith('audio/') || cleanPath.endsWith('.pdf')) {
      return `${viteBase}${cleanPath}`;
    }
    return `/${language}/${cleanPath}`;
  }, [language]);

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, getAssetUrl }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
